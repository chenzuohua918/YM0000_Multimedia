package com.semisky.ym_multimedia.ymbluetooth.data;

/**
 * 电话本的工具类
 * Created by DaiShiHao on 2015/1/19 0019.
 */
public class PhoneBookInfos {

    private String pBookName;
    private String pBookNum;
    private String sortLetter;

    public String getpBookName() {
        return pBookName;
    }

    public void setpBookName(String pBookName) {
        this.pBookName = pBookName;
    }

    public String getpBookNum() {
        return pBookNum;
    }

    public void setpBookNum(String pBookNum) {
        this.pBookNum = pBookNum;
    }

    public String getSortLetter() {
        return sortLetter;
    }

    public void setSortLetter(String sortLetter) {
        this.sortLetter = sortLetter;
    }
}
